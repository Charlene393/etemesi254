enum class ZilImageFormat {
    /**
     * Any unknown format
     */
    UnknownFormat,

    /**
     * Joint Photographic Experts Group
     */
    JPEG,

    /**
     * Portable Network Graphics
     */
    PNG,

    /**
     * Portable Pixel Map image
     */
    PPM,

    /**
     * Photoshop PSD component
     */
    PSD,

    /**
     * Farbfeld format
     */
    Farbfeld,

    /**
     * Quite Okay Image
     */
    QOI,

    /**
     * JPEG XL, new format
     */
    JPEG_XL,

    /**
     * Radiance HDR decoder
     */
    HDR,

    /**
     * Windows Bitmap Files
     */
    BMP
}
